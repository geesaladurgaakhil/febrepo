package stringPrograms;

public class IndexOf 
{
	public static void main(String[] args)
	{
		String s = "DurgaAkhil";
		System.out.println(s.indexOf('A'));              // output is = 5//
		System.out.println(s.indexOf('q'));             //output = -1; because 'q' is not present in the given string//
		
	}

}
